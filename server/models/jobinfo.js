import mongoose from 'mongoose';

const jobSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var jobDescription = mongoose.model('jobDescription', jobSchema);

export default jobDescription;